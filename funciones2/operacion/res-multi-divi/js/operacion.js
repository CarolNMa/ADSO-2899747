function restar(pnumeroUno, pnumeroDos) {

    let num1 = pnumeroUno;
    let num2 = pnumeroDos;
    let resta;

    resta = num1 - num2;

    return resta;
}

function multiplicacion(pnumeroUno, pnumeroDos) {

    let num1 = pnumeroUno;
    let num2 = pnumeroDos;
    let multi;

    multi = num1 * num2;

    return multi;
}

function division(pnumeroUno, pnumeroDos) {

    let num1 = pnumeroUno;
    let num2 = pnumeroDos;
    let dividir;

    dividir = num1 / num2;

    return dividir;
}


