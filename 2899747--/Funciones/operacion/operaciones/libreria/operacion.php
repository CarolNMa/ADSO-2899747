<?php

function restar($numero1,$numero2){
    $nume1=$numero1;
    $nume2=$numero2;
    $resta = $nume1 - $nume2;
    return $resta;
}

function multiplicar($numero1,$numero2){
    $nume1=$numero1;
    $nume2=$numero2;
    $multiplicacion = $nume1 * $nume2;
    return $multiplicacion;
}

function division($numero1,$numero2){
    $nume1=$numero1;
    $nume2=$numero2;
    $dividir = $nume1 / $nume2;
    return $dividir;
}

